package com.example.demo.model;

public class Demo {

    private int idDemo;
    private String nameDemo;

    public Demo(int idDemo, String nameDemo) {
        this.idDemo = idDemo;
        this.nameDemo = nameDemo;
    }

    public int getIdDemo() {
        return idDemo;
    }

    public void setIdDemo(int idDemo) {
        this.idDemo = idDemo;
    }

    public String getNameDemo() {
        return nameDemo;
    }

    public void setNameDemo(String nameDemo) {
        this.nameDemo = nameDemo;
    }
}
